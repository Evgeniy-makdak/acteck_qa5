P("Вычисление P(D) выполняется за O(n log n) операций:")

Pn("<b>Шаг 1.</b> Формирование массива размеров исторических сегментов "
   "в пунктах: L[i] = pips<sub>i</sub> × pt, i = 1…n.")

Pn("<b>Шаг 2.</b> Сортировка массива L по убыванию.")

Pn("<b>Шаг 3.</b> Вычисление пройденного расстояния D = |C − S|.")

Pn("<b>Шаг 4.</b> Нахождение рангового индекса k — первого индекса, "
   "для которого L[k] ≤ D. Если все L[i] > D, то k = n + 1 и P(D) = 0%.")

Pn("<b>Шаг 5.</b> Вычисление вероятности: P(D) = ⌊(k / n) × 100⌋.")

